#include <stdio.h>

int main() {
int a = 5; // กำหนดค่าเริ่มต้นให้กับตัวแปร a
int b = 2; // กำหนดค่าเริ่มต้นให้กับตัวแปร b
float x = 3.0; // กำหนดค่าเริ่มต้นให้กับตัวแปร x
float y = 4.5; // กำหนดค่าเริ่มต้นให้กับตัวแปร y

int r1, r2; // กำหนดตัวแปรสำหรับเก็บผลลัพธ์ของนิพจน์
float r3, r4; // กำหนดตัวแปรสำหรับเก็บผลลัพธ์ของนิพจน์ที่เป็นจำนวนทศนิยม

printf("Results of Advanced Expressions:\n\n"); // แสดงข้อความเริ่มต้น 
printf("Initial values: a = 5, b = 2, x = 3.0, y = 4.5\n\n"); // แสดงค่าตัวแปรเริ่มต้น

r1 = a++ * b + (int)y % 3; // คำนวณ r1 โดยใช้ a, b และ y
printf("1. int r1 = a++ * b + (int)y %% 3;\n"); // แสดงนิพจน์ที่ใช้ในการคำนวณ r1
printf(" - r1 = %d\n", r1); // แสดงผลลัพธ์ของ r1
printf(" - Value of a after calculation: %d\n\n", a); // แสดงค่าของ a หลังจากการคำนวณ


r2 = (a > b) && ((int)x / b < 2); // คำนวณ r2 โดยใช้ a, b และ x
printf("2. int r2 = (a > b) && ((int)x / b < 2);\n"); // แสดงนิพจน์ที่ใช้ในการคำนวณ r2
printf(" - r2 = %d\n\n", r2); // แสดงผลลัพธ์ของ r2

r3 = ++x * y - a / 2; // คำนวณ r3 โดยใช้ x, y และ a
printf("3. float r3 = ++x * y - a / 2;\n"); // แสดงนิพจน์ที่ใช้ในการคำนวณ r3
printf(" - r3 = %.2f\n\n", r3); // แสดงผลลัพธ์ของ r3

r4 = ((x += 1.5) > y) || ((b--) > 0); // คำนวณ r4 โดยใช้ x, y และ b
printf("4. float r4 = ((x += 1.5) > y) || ((b--) > 0);\n"); // แสดงนิพจน์ที่ใช้ในการคำนวณ r4
printf(" - r4 = %.2f\n", r4); // แสดงผลลัพธ์ของ r4
printf(" - Value of b after calculation: %d\n", b); // แสดงค่าของ b หลังจากการคำนวณ

return 0;
}

