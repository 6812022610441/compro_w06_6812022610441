#include <stdio.h>

int main() {
int A, B, C, D, E, F, G; // กำหนดตัวแปรสำหรับเก็บผลลัพธ์ของนิพจน์

printf("ผลลัพธ์ของการคำนวณนิพจน์ตามลำดับความสำคัญ:\n\n"); // แสดงข้อความเริ่มต้น

A = -2 + 5 * 2; // คำนวณ A = -2 + 5 * 2 คือ -2 + 10
printf("A = -2 + 5 * 2 = %d\n", A); // แสดงผลลัพธ์ของ A

B = 10 / 2 * 3; // คำนวณ B = 10 / 2 * 3 คือ 5 * 3
printf("B = 10 / 2 * 3 = %d\n", B); // แสดงผลลัพธ์ของ B

C = 6 / 2 + 3 * (4 % 2); // คำนวณ C = 6 / 2 + 3 * (4 % 2) คือ 3 + 3 * 0
printf("C = 6 / 2 + 3 * (4 %% 2) = %d\n", C); // แสดงผลลัพธ์ของ C

D = (5+2) * 15 % 4; // คำนวณ D = (5+2) * 15 % 4 คือ 7 * 15 % 4
printf("D = (5+2) * 15 %% 4 = %d\n", D); // แสดงผลลัพธ์ของ D

E = 6 + 2 * 2 - 6 / 2; // คำนวณ E = 6 + 2 * 2 - 6 / 2 คือ 6 + 4 - 3
printf("E = 6 + 2 * 2 - 6 / 2 = %d\n", E); // แสดงผลลัพธ์ของ E

F = 5 + 3 * 2 - 8 / 4 + (6 % 5); // คำนวณ F = 5 + 3 * 2 - 8 / 4 + (6 % 5) คือ 5 + 6 - 2 + 1   
printf("F = 5 + 3 * 2 - 8 / 4 + (6 %% 5) = %d\n", F); // แสดงผลลัพธ์ของ F

G = (4+3) * 2 - 10 / (2+3); // คำนวณ G = (4+3) * 2 - 10 / (2+3) คือ 7 * 2 - 10 / 5
printf("G = (4+3) * 2 - 10 / (2+3) = %d\n", G); // แสดงผลลัพธ์ของ G

return 0;
}
